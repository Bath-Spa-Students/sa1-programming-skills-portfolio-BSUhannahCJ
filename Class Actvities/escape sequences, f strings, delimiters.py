# end delimited to remove new line, by default print will to another statement
uh = "Hannah Judeh"
print (uh, end ="")
print(" new print line", end ="")

# \n and \t these are also known as escape sequences
print ("\n this line will print on next line")
print ("\t this line will be tabbed")

#f strings
student = "hannah"
print(f"Your name is {student}")
