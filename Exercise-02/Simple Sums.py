#Exercise 2: simple sums

"""Steps:
1. Declare a variable and initialize it with the integer value `8`.
2. Declare a second variable and initialize it with the integer value `10`.
3. Declare a third variable that stores the sum of first two numbers.
4. Print the value of the sum to the console.
"""

Hi = 8 #declaring the variable 'Hi' with an integer value of 8 as the first operant
Hey = 10 #declaring the variable 'Hey' with an integer value of 10 as the second operant
Hello = Hi + Hey #Variable 'Hello' is declared as the sum of 'Hi' and 'Hey' by using the + operator
print (Hello) #so when we print C, it will print the sum of both variables